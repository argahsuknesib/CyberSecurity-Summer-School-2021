package com.xiaomi.account.auth;

class OrderApp {
    private String name;
    private int version;

    OrderApp() {
    }

    public String getName() {
        return this.name;
    }

    public void setName(String str) {
        this.name = str;
    }

    public int getVersion() {
        return this.version;
    }

    public void setVersion(int i) {
        this.version = i;
    }
}
